void main() {
  // define
  var i = 0;
  // condition
  while (i < 5) {
    print(" ${i + 1} Hello");
// assignment operator
    i++; // increased the value of i by 1
  }

// do while
  do {
    print(" $i Hello");
i++;
  } while (i < 6);

// for 
  for(num i =0 ; i> 11 ; i++){
    print('$i Hello');
  }
}
